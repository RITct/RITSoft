<p  align="left">RITSoft is a (still developing) software designed and hosted by the Department of Computer Applications of Rajiv Gandhi Institute of Technology, Kottayam.</p>
<p>The first module of RITSoft (Admission & TC) was developed in 2015, by Krishna Prasad K. of 2012-15 MCA Batch, as his Mini Project. Modelled after the Admission part of CampusSoft from College of Engineering Trivandrum, it was designed as a simple remote Database application with a Python interface. The main motivation behind the project was a request from Mr. Jose, Junior Supdt. who was in charge of Admissions, to Dr. C. Sathish Kumar, then the Head of the Department.</p>
<p>In 2016, the first version of RITSoft, was partly completed by Ashwani Mohan, Nithin P, Rajalekshmy V S, Resmi S, Robert Joseph and Soumya Joy of 2013-16 MCA Batch. This is when RITSoft grew to the form of a complete web application written in PHP. The underlying database was an extension to the earlier one. </p>
<p>In 2018, around 50 students of 2015-18 MCA Batch took up RITSoft as their Mini Project. By the grace of Almighty (I believe so!!), they got around one month's time dedicated for Mini Project, due to an unexpected delay in the conduct of Fifth Semester University Examinations. Although the entire class worked hard, a few of them, including Mohammed Dilruba P, Manu Mohan and Iby Tresa Augustine, co-ordianted the whole work and realised a long cherished dream of the entire RIT Community - a Campus Software for RIT.</p>
<p> Later, Indrajith C of 2016-19 MCA Batch took up Ritsoft as his mini project, successfully completed the incorporation of new requirements and solved the real-life issues arose while using Ritsoft.</p>
<p>The Administration of RITSoft V2 is carried out by a committee lead by Dr. David Solomon George, Professor, ECE Dept. The code is maintained by Ms. Sonupriya P.S., Asst. Professor (Adhoc), Department of Computer Applications.</p>
<p>The project, which is the most remarkable achievement of the Department of Computer Applications, needs a lot of Coding and Testing. You can contribute to this big project and get your name added to this list by finding Bugs, Vulnerabilities or by involving in the Development. For details, mail to ritsoft AT rit.ac.in.  </p>
